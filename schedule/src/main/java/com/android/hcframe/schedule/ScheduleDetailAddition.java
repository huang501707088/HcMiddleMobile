public class ScheduleDetailAddition {

    private  String mAdditionId;

    private String mAdditionName;

    private String isAdditionVisible;

    public ScheduleDetailAddition() {
        super();
    }

    public String getmAdditionId() {
        return mAdditionId;
    }

    public void setmAdditionId(String mAdditionId) {
        this.mAdditionId = mAdditionId;
    }

    public String getmAdditionName() {
        return mAdditionName;
    }

    public void setmAdditionName(String mAdditionName) {
        this.mAdditionName = mAdditionName;
    }

    public String getIsAdditionVisible() {
        return isAdditionVisible;
    }

    public void setIsAdditionVisible(String isAdditionVisible) {
        this.isAdditionVisible = isAdditionVisible;
    }
}
