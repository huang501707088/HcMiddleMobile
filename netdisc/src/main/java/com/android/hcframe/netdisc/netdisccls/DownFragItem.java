public class DownFragItem {
    private String  downImg;
    private String downFileName;
    private String downFileM;
    private String downDate;
    private String downTime;

    public DownFragItem() {
        super();
    }

    public String getDownImg() {
        return downImg;
    }

    public void setDownImg(String downImg) {
        this.downImg = downImg;
    }

    public String getDownFileName() {
        return downFileName;
    }

    public void setDownFileName(String downFileName) {
        this.downFileName = downFileName;
    }

    public String getDownFileM() {
        return downFileM;
    }

    public void setDownFileM(String downFileM) {
        this.downFileM = downFileM;
    }

    public String getDownDate() {
        return downDate;
    }

    public void setDownDate(String downDate) {
        this.downDate = downDate;
    }

    public String getDownTime() {
        return downTime;
    }

    public void setDownTime(String downTime) {
        this.downTime = downTime;
    }
}
