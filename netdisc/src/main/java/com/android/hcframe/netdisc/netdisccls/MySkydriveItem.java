public class MySkydriveItem {
    private String netdiscListId;
    private String netdiscListImg;
    private String netdiscListText;
    private String netdiscListDate;
    private String netdiscListType;
    private String netdiscListLvl;
    public MySkydriveItem() {
        super();
    }

    public String getNetdiscListId() {
        return netdiscListId;
    }

    public void setNetdiscListId(String netdiscListId) {
        this.netdiscListId = netdiscListId;
    }

    public String getNetdiscListImg() {
        return netdiscListImg;
    }

    public void setNetdiscListImg(String netdiscListImg) {
        this.netdiscListImg = netdiscListImg;
    }

    public String getNetdiscListText() {
        return netdiscListText;
    }

    public void setNetdiscListText(String netdiscListText) {
        this.netdiscListText = netdiscListText;
    }

    public String getNetdiscListDate() {
        return netdiscListDate;
    }

    public void setNetdiscListDate(String netdiscListDate) {
        this.netdiscListDate = netdiscListDate;
    }

    public String getNetdiscListType() {
        return netdiscListType;
    }

    public void setNetdiscListType(String netdiscListType) {
        this.netdiscListType = netdiscListType;
    }

    public String getNetdiscListLvl() {
        return netdiscListLvl;
    }

    public void setNetdiscListLvl(String netdiscListLvl) {
        this.netdiscListLvl = netdiscListLvl;
    }
}
